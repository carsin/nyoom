export class CarBrands{
    brandName: string;
    link: string;
    width: string;
    height: string;
    picture: string;

    constructor(brandName: string, link: string, height: string, width: string, picture: string){
        this.brandName = brandName;
        this.link = link;
        this.width = width;
        this.height = height;
        this.picture = picture;
    }

}